import React, { useState, useEffect } from 'react';
import { Table, TableHead, TableBody, TableCell, TableRow, TableSortLabel, TablePagination } from '@mui/material';
import axios from 'axios';
import EditBtn from './EditBtn.svg';
import DeleteBtn from './DeleteBtn.svg';
import Placeholder from './placeholder.png';

const Test = () => {
  //const [data, setData] = useState([]);
  const [page, setPage] = useState(0);
  const [rowsPerPage, setRowsPerPage] = useState(12);
  const [orderBy, setOrderBy] = useState('fullname');
  const [order, setOrder] = useState('asc');
  const [contacts, setContacts] = useState([])

  useEffect(() => {
    getContactList();
  }, []);

  const getContactList = () => {
    axios.get("/api/Contacts")
      .then((response) => {
        setContacts(response.data);
      })
      .catch(error => console.log(error));
  }
  const handleChangePage = (event, newPage) => {
    setPage(newPage);
  };

  const handleUpdateContact = (contactInfo) => {
    //setMain("update");
    //setContactInfo(contactInfo)
  }

  const handleClickOpen = (contact) => {
    //setOpen(true);
    //setContactInfo(contact);
  };

  const handleChangeRowsPerPage = (event) => {
    setRowsPerPage(parseInt(event.target.value, 10));
    setPage(0);
  };

  const handleRequestSort = (property) => {
    const isAsc = orderBy === property && order === 'asc';
    setOrder(isAsc ? 'desc' : 'asc');
    setOrderBy(property);
  };

  const sortedContacts = contacts.slice().sort((a, b) => {
    if (order === 'asc') {
      return a[orderBy] > b[orderBy] ? 1 : -1;
    } else {
      return a[orderBy] < b[orderBy] ? 1 : -1;
    }
  });

  const emptyRows = rowsPerPage - Math.min(rowsPerPage, contacts.length - page * rowsPerPage);

  return (
    <div>
      <Table size="small">
        <TableHead>
          <TableRow>
            <TableCell style={{ width: 50 }}></TableCell>
            <TableCell style={{ width: 150 }}>
              <TableSortLabel
                active={orderBy === 'fullName'}
                direction={orderBy === 'fullName' ? order : 'asc'}
                onClick={() => handleRequestSort('fullName')}
              >
                Full Name
              </TableSortLabel>
            </TableCell>
            <TableCell style={{ width: 100 }}>
                Phone
            </TableCell>
            <TableCell style={{ width: 100 }}>
                Email
            </TableCell>
            <TableCell style={{ width: 300 }}>
                Address
            </TableCell>
            <TableCell style={{ width: 100 }}>
                Actions
            </TableCell>
            {/* Add more table headers based on your API data */}
          </TableRow>
        </TableHead>
        <TableBody>
          {sortedContacts
            .slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
            .map((contact) => (
              <TableRow>
                <TableCell><img src={Placeholder} alt="picture" className="placeholder" /></TableCell>
                <TableCell>{contact.fullName}</TableCell>
                <TableCell>{contact.phone}</TableCell>
                <TableCell>{contact.email}</TableCell>
                <TableCell>{contact.address}</TableCell>
                <TableCell>
                    <div>
                      <img className="small-btn" src={EditBtn} onClick={() => handleUpdateContact(contact)} />
                      <span variant="text" onClick={() => handleClickOpen(contact)}><img src={DeleteBtn} className="small-btn" /></span>
                    </div>
                  </TableCell>
                </TableRow>
            ))}
          {emptyRows > 0 && (
            <TableRow style={{ height: 49 * emptyRows }}>
              <TableCell colSpan={6} />
            </TableRow>
          )}
        </TableBody>
      </Table>
      <TablePagination
        rowsPerPageOptions={12}
        component="div"
        count={contacts.length}
        rowsPerPage={rowsPerPage}
        page={page}
        onPageChange={handleChangePage}
        onRowsPerPageChange={handleChangeRowsPerPage}
      />
    </div>
  );
};

export default Test;




